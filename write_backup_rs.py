# normalize_path_for_backup
lines.append('/// Normalize a path for backup operations')
lines.append('fn normalize_path_for_backup(path: &str) -> Result<String, AppError> {')
lines.append('    let validated = validate_backup_restore_path(path, false)?;')
lines.append('    if is_system_sensitive_directory(&validated) {')
lines.append('        return Err(AppError::validation("不允许备份系统敏感目录"));')
lines.append('    }')
lines.append('    let normalized = validated.to_string_lossy().to_string();')
lines.append('    info!("Normalized backup path: {}", normalized);')
lines.append('    Ok(normalized)')
lines.append('}')
