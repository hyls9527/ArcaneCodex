# Helper functions
lines.append('/// Check if a path component is a directory traversal attempt')
lines.append('fn contains_encoded_traversal(path: &str) -> bool {')
lines.append('    let encoded_patterns = ["%2e", "%252e", "..%5c", "%c0%ae", "%e0%40%ae", "%c0%2e", "%%32%%65"];')
lines.append('    let lower = path.to_lowercase();')
lines.append('    encoded_patterns.iter().any(|&p| lower.contains(p))')
lines.append('}')
lines.append('')

# Now the big function
lines.append('/// Validate backup/restore path for security')
lines.append('fn validate_backup_restore_path(path: &str, must_exist: bool) -> Result<PathBuf, AppError> {')
lines.append('    let input = path.trim();')
lines.append('    if input.is_empty() {')
lines.append('        return Err(AppError::validation("路径不能为空"));')
lines.append('    }')
lines.append('    if input.len() > 240 {')
lines.append('        return Err(AppError::validation(format!("路径过长 ({} 字符，最大 240)", input.len())));')
lines.append('    }')
lines.append('    let path_buf = PathBuf::from(input);')
lines.append('    let input_lower = input.to_lowercase();')
lines.append('    if input.starts_with("\\\\") || input.starts_with("//") {')
lines.append('        return Err(AppError::validation("不允许使用 UNC 路径"));')
lines.append('    }')
lines.append('    if input_lower.contains("\\\\.\\") || input_lower.contains("\\\\?\\") {')
lines.append('        return Err(AppError::validation("不允许使用设备路径"));')
lines.append('    }')
lines.append('    if input.contains("::$") {')
lines.append('        return Err(AppError::validation("检测到 NTFS 数据流攻击"));')
lines.append('    }')
lines.append('    #[cfg(windows)]')
lines.append('    {')
lines.append('        const RESERVED_NAMES: &[&str] = &[')
lines.append('            "con", "prn", "aux", "nul", "com1", "com2", "com3", "com4", "com5", "com6", "com7", "com8", "com9",')
lines.append('            "lpt1", "lpt2", "lpt3", "lpt4", "lpt5", "lpt6", "lpt7", "lpt8", "lpt9",')
lines.append('        ];')
lines.append('        if let Some(file_name) = path_buf.file_name().and_then(|n| n.to_str()) {')
lines.append('            let name_lower = file_name.to_lowercase();')
lines.append('            let name_without_ext = if let Some(dot) = name_lower.rfind(".") { &name_lower[..dot] } else { &name_lower };')
lines.append('            if RESERVED_NAMES.contains(&name_without_ext) {')
lines.append('                return Err(AppError::validation("路径包含 Windows 保留名称"));')
lines.append('            }')
lines.append('        }')
lines.append('    }')
lines.append('    // Check for directory traversal')
lines.append('    for component in path_buf.components() {')
lines.append('        if component == Component::ParentDir {')
lines.append('            return Err(AppError::validation("路径包含遍历序列 (..)"));')
lines.append('        }')
lines.append('    }')
lines.append('    // Check for encoded traversal')
lines.append('    if contains_encoded_traversal(input) {')
lines.append('        return Err(AppError::validation("检测到编码路径遍历攻击"));')
lines.append('    }')
lines.append('    // Check for control characters')
lines.append('    if input.bytes().any(|b| b < 32 && b != 9 && b != 10 && b != 13) {')
lines.append('        return Err(AppError::validation("路径包含控制字符"));')
lines.append('    }')
lines.append('    // Check for must_exist')
lines.append('    if must_exist && !path_buf.exists() {')
lines.append('        return Err(AppError::validation("路径不存在"));')
lines.append('    }')
lines.append('    Ok(path_buf)')
lines.append('}')
lines.append('')

# is_system_sensitive_directory
lines.append('/// Check if a path contains system-sensitive directories')
lines.append('fn is_system_sensitive_directory(path: &Path) -> bool {')
lines.append('    let sensitive_dirs = [')
lines.append('        "/etc", "/sys", "/proc", "/dev", "/boot", "/root", "/var/log",')
lines.append('    ];')
lines.append('    let path_str = path.to_string_lossy().to_lowercase().replace("\\\\", "/");')
lines.append('    for dir in &sensitive_dirs {')
lines.append('        if path_str.starts_with(dir) || path_str.contains(&format!("/{}/", dir.trim_start_matches("/"))) {')
lines.append('            return true;')
lines.append('        }')
lines.append('    }')
lines.append('    false')
lines.append('}')
lines.append('')

